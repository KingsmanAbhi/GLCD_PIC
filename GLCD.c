void GLCD_Init(void)
{
   CS_Pin = 0;
   RES_Pin = 1;
   RES_Pin = 0;
   __delay_ms(2);
   RES_Pin = 1;
   __delay_ms(2);
 
   GLCD_WriteCMD(0xA2);        

   GLCD_WriteCMD(0xA0);     

   GLCD_WriteCMD(0xC8);      
 
   GLCD_WriteCMD(0xC0);      
 
   GLCD_WriteCMD(0x40);      

   __delay_ms(0);
  
   GLCD_WriteCMD(0x25);      

   __delay_ms(0);
 
   GLCD_WriteCMD(0x81);      

   __delay_ms(0);
 
   GLCD_WriteCMD(0x19);      

   __delay_ms(0);
  
   GLCD_WriteCMD(0x2F);      

   __delay_ms(0);
   
   GLCD_WriteCMD(0x3f);      

   __delay_ms(0);

   GLCD_WriteCMD(0xAF);      // Display ON  
    
}



void GLCD_WriteCMD(uint8_t cmd)
{
    A0_Pin = 0;
    CS_Pin = 0;
//    RES_Pin = 0;
    SPI_WriteByte(cmd);
    //while(!SSPSTATbits.BF);
    CS_Pin = 1;
}

void GLCD_WriteData(uint8_t data)
{
    A0_Pin = 1;
    CS_Pin = 0;
//    RES_Pin = 0;
    SPI_WriteByte(data);
   // while(!SSPSTATbits.BF);
    CS_Pin = 1;
}

/* set the cursor to perticular page and coloumn */
void LCD_SET_PAGE(unsigned char page,unsigned char col)
{
   unsigned char msb,lsb;
   msb      =   (((col & 0xF0) >> 4)| 0x10);
   lsb      =   (col & 0x0F);
   GLCD_WriteCMD(page);
   __delay_ms(1);
   GLCD_WriteCMD(msb);
   __delay_ms(1);
   GLCD_WriteCMD(lsb);
   __delay_ms(1);
}


/* clears entire lcd or clears all page*/
void LCD_CLEAR(void)
{
   unsigned char i = 0,j = 0;
   
   LCD_SET_PAGE(PAGE1,COL1);      // initial page, col
      
   for(i = 0; i < 8; i ++)
   {
          for(j = 0; j < 128; j ++)
            GLCD_WriteData(0x00);
          LCD_SET_PAGE(PAGE1 + i + 1 , COL1);   // increment page
   }
   
   LCD_SET_PAGE(PAGE1,COL1);      // initial page,col
       
}

void lcd_display_char(U8 charecter)
{
  U8 width = 0, index = 0, data = 0;
  
  width = get_width_of_char(charecter);
  for(index = 0; index < width; index++)
  {
    data = get_data_from_data_table(charecter, index);
    GLCD_WriteData(data);
  }
}

void lcd_display_string(U8 *str)
{
  if(str == NULL)
    return;
    
  while(*str != (U32)NULL)
  {
    lcd_display_char(*str);
    str++;
  }
}






